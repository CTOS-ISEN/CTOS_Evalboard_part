/**
 * @Brief Init all the required variables for the use of the FSM
 */
void init_fsm(void) {
	actual_state = 0;
	target_state = 0;

	state_callbacks[STATE_HANDLER] = state_start;
	state_callbacks[STATE_ACK_TOF] = state_ack_tof;
	state_callbacks[STATE_ACK_IMU] = state_ack_imu;
	state_callbacks[STATE_STORE] = state_store;
	state_callbacks[STATE_SEND] = state_send;

	set_new_state(STATE_HANDLER);
}

/**
 * @Brief Manage the FSM by changing the state if necessary
 */
void fsm_project(void) {
	state_callbacks[current_state]();

	execution_state = EXECUTED;

	switch (current_state) {

	case STATE_HANDLER:
		if (startACK_flag == 1) {
			set_new_state(STATE_ACK_TOF);
		}
		if (startSEND_flag == 1) {
			set_new_state(STATE_SEND);
		}
		break;

	case STATE_ACK_TOF:
		set_new_state(STATE_ACK_IMU);
		break;

	case STATE_ACK_IMU:
		set_new_state(STATE_STORE);
		break;

	case STATE_STORE:
		set_new_state(STATE_HANDLER);
		break;

	case STATE_SEND:
		set_new_state(STATE_HANDLER);
		break;

	}
}

